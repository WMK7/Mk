    private void jButton_CalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_CalcularActionPerformed
        // TODO add your handling code here:
        if(jTextField_Valor.getText() == ""){
            jTextField_Valor.requestFocus();
            return;
        }
        try {
            float valor = Float.parseFloat(jTextField_Valor.getText());
            if(jList_Descont.getSelectedIndex()== -1){
                JOptionPane.showMessageDialog(null,"Selecione um item da lista de desconto");
                return;
            }
            float desconto = 0.9f;
            if(jList_Descont.getSelectedIndex() == 1){
                desconto = 0.8f;
            }else if(jList_Descont.getSelectedIndex() == 2){
                desconto = 0.7f;
            }else if(jList_Descont.getSelectedIndex() == 3){
                desconto = 0.6f;
            }else if(jList_Descont.getSelectedIndex() == 4){
                desconto = 0.5f;    
            }
            jTextField_Resultado.setText(""+valor*desconto);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Forne√ßa Apenas Valores Numeros\n"+ e.toString());
            jTextField_Valor.requestFocus();
        }
    }//GEN-LAST:event_jButton_CalcularActionPerformed

